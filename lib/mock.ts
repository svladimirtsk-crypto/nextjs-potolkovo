  const place = ctx.roomLabel ? `для ${ctx.roomLabel.toLowerCase()}` : "для этого помещения";
  const lowHeight =
    Boolean(input.ceilingHeight && input.ceilingHeight < 250) ||
    Boolean(ctx.heightNote?.toLowerCase().includes("минимальной"));
  const isWet = ctx.isWetRoom;

  const isTrack = includesAny(q, ["трек", "магнитн", "спот"]);
