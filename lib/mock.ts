    return {
      scenario: "tech-question",
      intent: "вопрос по нише под карниз",
      shortAnswer: `Да, нишу под карниз ${place} сделать можно, и её лучше сразу закладывать в конструкцию потолка. Ключевое — правильно выбрать глубину, ширину и отступ, чтобы шторы свободно ходили и не тёрлись о полотно.`,
      recommendedOptions: [
        {
          name: "Основной вариант",
          description:
            "Скрытая ниша под карниз с заранее рассчитанной глубиной под нужное количество направляющих и тип штор.",
        },
        {
          name: "Альтернатива",
          description:
            "Открытый карниз без ниши, если нужно уменьшить бюджет и упростить конструкцию потолка.",
        },
      ],
      whatToConsider: unique([
        "Обычно глубина ниши берётся порядка 10–15 см, но фактически зависит от карниза и штор.",
        "Если нужна подсветка в нише, сразу нужно предусмотреть питание и обслуживание.",
        ...commonNotes,
      ]),
      estimatedImpact: {
        heightLoss: "обычно 5–7 см по потолку плюс зона ниши",
        budgetNote: "стоимость зависит от длины ниши, подсветки, типа профиля и количества шторных рядов",
      },
      nextStep: "Если знаешь тип штор и карниза, можно быстро понять, какой размер ниши нужен именно в твоём случае.",
    };
